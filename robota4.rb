#Реалізуйте метод, який повертатиме true, якщо заданий
#аргумент строки є IPv4-адресою - чотири числа (0-255),
#розділені крапками. Він повинен приймати лише адреси в
#канонічному представленні, тобто без ведучих нулів,
#пропусків тощо

def valid_ipv4?(ip)
    # Розбиваємо рядок на частини, використовуючи крапки як роздільник
    parts = ip.split('.')
  
    # Перевіряємо, чи складається рядок з чотирьох частин
    return false unless parts.length == 4
  
    # Перевіряємо кожну частину на відповідність критеріям: число від 0 до 255, без ведучих нулів
    parts.each do |part|
      # Частина повинна бути цілим числом, яке не містить ведучих нулів
      return false unless part.match?(/\A[1-9]?[0-9]{1,2}\z|^0$/) && part.to_i.between?(0, 255)
    end
  
    true
  end
  
  # Тестові приклади
  puts valid_ipv4?("192.168.1.1")       # true
  puts valid_ipv4?("0.0.0.0")           # true
  puts valid_ipv4?("255.255.255.255")   # true
  puts valid_ipv4?("192.168.01.1")      # false (ведучі нулі)
  puts valid_ipv4?("256.100.50.25")     # false (256 більше за 255)
  puts valid_ipv4?("192.168.1")         # false (не вистачає частин)
  puts valid_ipv4?("192.168.1. 1")      # false (пробіл між частинами)
  puts valid_ipv4?("192.168.a.1")       # false (нечислове значення)
  